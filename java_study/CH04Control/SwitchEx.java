package Control;

public class SwitchEx {
    public static void main(String[] args) {
        int num = 10;

        switch (num){
            case 10:
                System.out.println("해당 숫자: " + num);
                break;
            case 9:
                System.out.println("해당 숫자: " + num);
                break;
            default:
                System.out.println("해당 숫자가 아닙니다.");
        }
    }
}
